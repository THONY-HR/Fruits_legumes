
function showPopup() {
    const popup = document.getElementById('popup');
    popup.style.display = 'flex';
  }
  
  function closePopup() {
    const popup = document.getElementById('popup');
    popup.style.display = 'none';
  }
  
  function logout() {
    window.location.href = 'deconnexion.html';
  }
  